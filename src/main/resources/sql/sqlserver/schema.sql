IF OBJECT_ID('dbo.T_SYS_CONFIG', 'U') IS NULL
BEGIN
    CREATE TABLE dbo.T_SYS_CONFIG (
        CONFIG_CODE NVARCHAR(80) NOT NULL,
        CONFIG_NAME NVARCHAR(200) NULL,
        REMARK NVARCHAR(1000) NULL,
        CREATED_BY NVARCHAR(50) NULL,
        CREATED_TIME DATETIME2 NULL,
        UPDATED_BY NVARCHAR(50) NULL,
        UPDATED_TIME DATETIME2 NULL,
        CONSTRAINT PK_T_SYS_CONFIG PRIMARY KEY (CONFIG_CODE)
    );
END;

