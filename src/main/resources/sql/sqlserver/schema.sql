IF OBJECT_ID('dbo.T_HOT_RELOAD_TASK', 'U') IS NULL
BEGIN
    CREATE TABLE dbo.T_HOT_RELOAD_TASK (
        TASK_ID NVARCHAR(80) NOT NULL,
        APP_NAME NVARCHAR(100) NOT NULL,
        ENV NVARCHAR(30) NOT NULL,
        PATCH_NAME NVARCHAR(300) NULL,
        PATCH_SHA256 NVARCHAR(128) NULL,
        RELOAD_TYPE NVARCHAR(30) NULL,
        PERSIST_ON_RESTART NVARCHAR(1) NULL CONSTRAINT DF_T_HOT_RELOAD_TASK_PERSIST DEFAULT 'N',
        TASK_REMARK NVARCHAR(1000) NULL,
        CREATED_BY NVARCHAR(50) NULL,
        CREATED_TIME DATETIME2 NULL,
        UPDATED_BY NVARCHAR(50) NULL,
        UPDATED_TIME DATETIME2 NULL,
        CONSTRAINT PK_T_HOT_RELOAD_TASK PRIMARY KEY (TASK_ID)
    );
END;

IF NOT EXISTS (SELECT 1 FROM sys.indexes WHERE name = 'IDX_HOT_RELOAD_TASK_01' AND object_id = OBJECT_ID('dbo.T_HOT_RELOAD_TASK'))
BEGIN
    CREATE INDEX IDX_HOT_RELOAD_TASK_01 ON dbo.T_HOT_RELOAD_TASK (ENV, APP_NAME);
END;

